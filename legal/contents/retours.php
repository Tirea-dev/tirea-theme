<section class="tirea-legal-section" id="s01">
  <span class="tirea-legal-section-num">01</span>
  <h2>Délai de rétractation</h2>
  <p>Conformément aux dispositions de l'article L.221-18 du Code de la consommation, vous disposez d'un délai légal de <strong>quatorze (14) jours francs</strong> pour exercer votre droit de rétractation, sans avoir à justifier de motifs ni à payer de pénalités.</p>
  <p>Ce délai court à compter du lendemain de la réception physique de votre commande par vous-même ou par un tiers désigné.</p>
</section>

<section class="tirea-legal-section" id="s02">
  <span class="tirea-legal-section-num">02</span>
  <h2>Conditions d'éligibilité</h2>
  <p>Pour que votre retour soit accepté et le remboursement validé, l'article doit nous être renvoyé dans son <strong>état d'origine strict</strong>. Il ne doit avoir été ni porté, ni lavé, ni altéré d'aucune manière.</p>
  <p>Il doit impérativement être accompagné de l'intégralité de ses accessoires, de ses étiquettes d'origine intactes et de son emballage initial en parfait état. TIREA se réserve le droit de refuser tout retour ne respectant pas ces conditions rigoureuses. Dans ce cas, le produit vous sera renvoyé à vos frais.</p>
  <div class="tirea-legal-callout">
    <span class="tirea-legal-callout-label">Articles exclus</span>
    <p>Les articles personnalisés, réalisés sur mesure ou descellés ne pouvant être renvoyés pour des raisons d'hygiène sont exclus du droit de rétractation, conformément à l'article L.221-28 du Code de la consommation.</p>
  </div>
</section>

<section class="tirea-legal-section" id="s03">
  <span class="tirea-legal-section-num">03</span>
  <h2>Procédure de retour</h2>
  <p>Pour initier un retour, veuillez suivre ces étapes afin d'assurer un traitement fluide et rapide :</p>
  <ol class="tirea-legal-steps">
    <li>
      <span class="tirea-legal-step-num">1</span>
      <span class="tirea-legal-step-text">Notifiez notre service client de votre volonté de retourner un article en écrivant à <a href="mailto:sav@tirea.fr">sav@tirea.fr</a>, en précisant impérativement votre numéro de commande. Vous pouvez pour cela utiliser le formulaire de rétractation en section&nbsp;08.</span>
    </li>
    <li>
      <span class="tirea-legal-step-num">2</span>
      <span class="tirea-legal-step-text">Replacez l'article soigneusement dans son emballage d'origine.</span>
    </li>
    <li>
      <span class="tirea-legal-step-num">3</span>
      <span class="tirea-legal-step-text">Joignez le bon de livraison initial ou une copie de votre facture pour faciliter l'identification de votre colis.</span>
    </li>
    <li>
      <span class="tirea-legal-step-num">4</span>
      <span class="tirea-legal-step-text">Expédiez la commande à l'adresse postale qui vous sera communiquée par notre équipe lors de la validation de votre demande.</span>
    </li>
  </ol>
</section>

<section class="tirea-legal-section" id="s04">
  <span class="tirea-legal-section-num">04</span>
  <h2>Frais d'expédition et responsabilités</h2>
  <p>Dans le cadre de l'exercice du droit de rétractation, les <strong>frais de retour demeurent à la charge exclusive du client</strong>.</p>
  <div class="tirea-legal-callout">
    <span class="tirea-legal-callout-label">Notre recommandation</span>
    <p>Nous vous recommandons vivement d'opter pour une expédition avec numéro de suivi et remise contre signature. Le colis retourné reste sous votre entière responsabilité jusqu'à sa réception formelle dans nos locaux. TIREA ne saurait être tenue pour responsable en cas de perte, de vol ou de dégradation du colis lors de son acheminement retour.</p>
  </div>
</section>

<section class="tirea-legal-section" id="s05">
  <span class="tirea-legal-section-num">05</span>
  <h2>Modalités de remboursement</h2>
  <p>Dès réception et inspection de votre retour par notre service qualité, nous procéderons au remboursement des sommes versées, incluant les frais de livraison initiaux (sur la base du mode de livraison standard le moins onéreux proposé sur notre site).</p>
  <p>Le remboursement sera déclenché dans un délai maximal de <strong>quatorze (14) jours</strong> suivant la réception de l'article dans nos entrepôts, ou jusqu'à ce que vous ayez fourni une preuve d'expédition. Il sera effectué automatiquement via le mode de paiement utilisé lors de la transaction initiale, sauf accord exprès de votre part pour un autre moyen.</p>
  <p>Un e-mail de confirmation vous sera envoyé dès que le virement aura été ordonné.</p>
</section>

<section class="tirea-legal-section" id="s06">
  <span class="tirea-legal-section-num">06</span>
  <h2>Politique d'échange</h2>
  <p>Afin de vous garantir la meilleure disponibilité de notre catalogue, <strong>nous ne pratiquons pas d'échange standard</strong>. Si vous souhaitez obtenir un article dans une autre taille ou un autre coloris, nous vous invitons à procéder au retour de la pièce initiale pour remboursement, puis à passer une nouvelle commande sur le site <a href="<?php echo esc_url(home_url('/')); ?>">tirea.fr</a>.</p>
</section>

<section class="tirea-legal-section" id="s07">
  <span class="tirea-legal-section-num">07</span>
  <h2>Articles défectueux ou erreurs de livraison</h2>
  <p>Le contrôle qualité est au cœur de notre processus d'expédition. Toutefois, si vous constatiez une erreur de préparation ou un dommage lié au transport sur l'article reçu, nous vous prions de contacter notre service client à <a href="mailto:sav@tirea.fr">sav@tirea.fr</a> dans un délai de <strong>48 heures après la livraison</strong>, photographies à l'appui.</p>
  <div class="tirea-legal-callout">
    <span class="tirea-legal-callout-label">Votre protection</span>
    <p>Cette démarche ne remet aucunement en cause votre droit de bénéficier de la garantie légale de conformité (2 ans) et de la garantie des vices cachés, telles que détaillées dans nos <a href="<?php echo esc_url(home_url('/cgv/')); ?>">Conditions Générales de Vente</a>. En cas de non-conformité avérée, les frais de retour et de réexpédition seront <strong>intégralement pris en charge par TIREA</strong>.</p>
  </div>
</section>

